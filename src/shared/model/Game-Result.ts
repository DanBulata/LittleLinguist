export class GameResult {
    idCategory: string;
    idGame: string;
    date: Date;
    numOfPoints: number;
    gameList?: { idCategory: string, idGame: string, date: Date, numOfPoints: number }[];
  
    // **Constructor accepts 4 parameters, and the optional gameList**
    constructor(
      idCategory: string,
      idGame: string,
      date: Date,
      numOfPoints: number,
      gameList?: { idCategory: string, idGame: string, date: Date, numOfPoints: number }[]
    ) {
      this.idCategory = idCategory;
      this.idGame = idGame;
      this.date = date;
      this.numOfPoints = numOfPoints;
      this.gameList = gameList || [];  // Default to empty array if gameList is undefined
    }
  }
  